export const getSetByCode = (code) => {
  return MTG_SETS[code?.toUpperCase()];
};

export const getAllSets = () => {
  return Object.values(MTG_SETS);
};

export const getSetIcon = (code) => {
  const set = getSetByCode(code);
  if (!set || set.icon_svg_uri?.includes('default.svg')) {
    return null;
  }
  return `/assets/set-icons/${set.code.toLowerCase()}_set.svg`;
};
